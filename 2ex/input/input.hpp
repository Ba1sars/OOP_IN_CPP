#ifndef H_INPUT
#define H_INPUT
#include <iostream>
#include <climits>

std::string readline(std::istream &in);

int readint(std::istream &in, int minumum = INT_MIN);

void process_error(std::istream &in);

template <typename T>
T readval(std::istream &in)
{
    T res;
    in >> res;
    while (!in.good())
    {
        process_error(in);
        in >> res;
    }
    return res;
}
#endif
